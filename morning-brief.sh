#!/bin/bash

# =============================================================================
# 晨间简报任务
# =============================================================================
# 功能：每天早晨发送综合简报（新闻 + 任务 + 推荐）
# 用法：直接被 task-framework.sh 调用
# =============================================================================

# 环境变量
export HOME="${HOME:-/home/zzm}"
export PATH="${PATH:-/usr/local/...}/bin"

# 配置（可通过环境变量覆盖）
FEISHU_USER="${FEISHU_USER:-ou_xxx}"
OPENCLAW_CMD="${OPENCLAW_CMD:-openclaw}"
CRON_DIR="${CRON_DIR:-${HOME:-/home/zzm}/.openclaw/cron}"
LOG_FILE="${CRON_DIR}/logs/morning-brief-$(date +%Y%m%d).log"

# 开始记录
echo "=== 晨间简报开始：$(date '+%Y-%m-%d %H:%M:%S') ===" | tee "$LOG_FILE"

# =============================================================================
# 1. 获取新闻摘要（示例：从本地文件或简单搜索）
# =============================================================================
# 注意：由于没有配置 web 搜索 API，这里用静态内容示例
# 你可以替换为实际的新闻源（RSS、网页搜索等）

NEWS_SECTION="## 📰 科技新闻摘要

**AI 领域**:
- OpenClaw 社区新增 40+ 用例
- 多代理协调模式成为趋势

**开发工具**:
- n8n 工作流编排最佳实践
- 凭证隔离模式推荐

*注：配置 web 搜索 API 后可获取实时新闻*"

echo "$NEWS_SECTION" >> "$LOG_FILE"

# =============================================================================
# 2. 获取今日任务（从飞书日历或任务清单）
# =============================================================================
# 这里用静态示例，实际可对接飞书日历 API

TASKS_SECTION="## ✅ 今日任务

**定时任务**:
- [ ] 08:00 仓库代码自动更新
- [ ] 08:05 OpenClaw 更新检查

**建议任务**:
- [ ] 学习晨间简报用例
- [ ] 优化定时任务框架

*注：可对接飞书日历/任务 API 获取真实任务*"

echo "$TASKS_SECTION" >> "$LOG_FILE"

# =============================================================================
# 3. AI 推荐任务（主动帮助）
# =============================================================================

RECOMMENDATIONS_SECTION="## 💡 AI 推荐

**今日可自动完成**:
1. 检查 AI 小弟服务状态
2. 清理 7 天前的日志文件
3. 生成昨日任务执行报告

**建议关注**:
- 定时任务成功率监控
- crontab 配置备份"

echo "$RECOMMENDATIONS_SECTION" >> "$LOG_FILE"

# =============================================================================
# 4. 输出完整报告（由 task-framework.sh 统一发送）
# =============================================================================

# 输出到 stdout，由 task-framework.sh 捕获并发送
cat <<EOF
## 🌅 晨间简报

**日期**: $(date '+%Y年%m月%d日 %A')
**发送时间**: $(date '+%H:%M')

---

$NEWS_SECTION

---

$TASKS_SECTION

---

$RECOMMENDATIONS_SECTION

---

**祝你今天高效愉快！** ☀️
EOF

echo "" >> "$LOG_FILE"
echo "=== 晨间简报完成：$(date '+%Y-%m-%d %H:%M:%S') ===" >> "$LOG_FILE"
